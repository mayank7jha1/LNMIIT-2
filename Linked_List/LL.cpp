#include<bits/stdc++.h>
using namespace std;
const int N = 0;

int main() {
	int a[] = {3, 4, 5, 6, 7};
	cout << a << endl;
	cout << a + 1 << endl;
	cout << *(a + 1) << endl;
	cout << a[1] << endl;

	// []-->Overloaded: value at this address.(Operator Overloading)

	int x = 20, y = 30;
	cout << x + y << endl;

	string m = "Mayank";
	string t = "Samyak";

	cout << m + t << endl;

	//+: data type
}

